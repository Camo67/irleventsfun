document.addEventListener('DOMContentLoaded', function() {
    // Animate feature items
    const featureItems = document.querySelectorAll('.irl-feature-item');
    featureItems.forEach((item, index) => {
        item.style.opacity = '0';
        item.style.transform = 'translateX(-20px)';
        item.style.transition = 'opacity 0.6s ease, transform 0.6s ease';
        
        setTimeout(() => {
            item.style.opacity = '1';
            item.style.transform = 'translateX(0)';
        }, index * 100);
    });

    // Add hover effects to testimonials
    const testimonials = document.querySelectorAll('[style*="border: 1px solid rgba(236, 72, 153, 0.3)"]');
    testimonials.forEach(testimonial => {
        testimonial.addEventListener('mouseenter', function() {
            this.style.borderColor = 'rgba(236, 72, 153, 0.6)';
            this.style.transform = 'scale(1.02)';
        });
        
        testimonial.addEventListener('mouseleave', function() {
            this.style.borderColor = 'rgba(236, 72, 153, 0.3)';
            this.style.transform = 'scale(1)';
        });
    });
});
